var color = 'red';
switch(color){
    case 'red':
        console.log('Red');
        break;
    case 'green':
        console.log("Green");
        break;
    case 'yellow':
        console.log("Yellow");
        break;
    case 'blue':
        console.log("Blue");
        break;
    default:
        console.log("Unknown");
        break;
}