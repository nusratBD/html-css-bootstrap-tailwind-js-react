/* 
Browser is a kind of desktop software that is used by installing in PC. The steps of showing a static website on a browser are:
1. Browser converts our html file into a tree which is known as dom tree.
2. To provide style on the html file, it creates a style tree.
3. DOM tree and style tree together creates the render tree which indicates the layout of the website. If any update includes in the DOM tree or the render tree, it re-renders on the same process. 
4. After getting render tree, it paints on it. If re-render happens, it does re-paint.
5. At last the output is displayed on the UI(User Interface).
*/