/* 
৫. local storage আর session storage এর মধ্যে তিনটা পার্থক্য লিখে ফেলো। 

Local Storage: 
1. The data don't delete when refrehse or visit to another site.
2. As it is not session base, the data must be deleted via JS or manually.
3. Its storage capacity is 5MB/to 10MB, and it can not send to DB.

Session Storage:
1. The data delete while refresh or visit to another site.
2. Data expires automatically while refresh or visit to another site.
3. Its storage capacity is 5MB, and it can not be send to DB.
*/