/* 
map: loop throw on the array elements, operate them, push the operated value in a new array, return the new array

forEach: just as for/for loop. loop throw the array elements, operate them. Do not push the operated value in a new array, and don't return the array.

filter: similar to map. loop throw on the array elements, filter the array values according to the condition, push the selected value in a new value, and return the new value.

find: similar to forEach: according to the condition, find only the first value, do not push it to a new array, just return the first value.
*/